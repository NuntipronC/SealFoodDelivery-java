    private void bt_addtimeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_addtimeActionPerformed
        //add time
        DefaultTableModel model = (DefaultTableModel)table_time.getModel();
        time[count_time][0] = (String)combo_sendername.getSelectedItem();
        time[count_time][1] = (String)combo_receivername.getSelectedItem();
        time[count_time][2] = txt_time.getText();
        for (int i = count_time;i <= count_time; i++){
            String row[] = new String[time[i].length];
            for (int j=0; j<time[i].length; j++){
                row[j] = time[i][j];
            }
            model.addRow(row);
        }
       count_time = count_time+1;
       
       
        
    }//GEN-LAST:event_bt_addtimeActionPerformed

