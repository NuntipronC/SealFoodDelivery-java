    private void bt_addsendernameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_addsendernameActionPerformed
        DefaultTableModel model = (DefaultTableModel)table_sendername.getModel();
        location[count_location][0] = txt_sendername.getText();
        for (int i = count_location;i <= count_location; i++){
            String row[] = new String[location[i].length];
            for (int j=0; j<location[i].length; j++){
                row[j] = location[i][j];
            }
            model.addRow(row);
        }
        count_location = count_location+1; 
    }//GEN-LAST:event_bt_addsendernameActionPerformed

